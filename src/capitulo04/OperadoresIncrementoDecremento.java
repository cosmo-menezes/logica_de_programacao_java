package capitulo04;

public class OperadoresIncrementoDecremento {

	public static void main(String[] args) {
		
		Integer numero = 10;
		Integer numero02 = ++numero;
		
		System.out.println("Número: " + numero + ", " + "Número02: " + numero02 );
		
		Integer number = 10;
		--number;
		System.out.println("Number: " + number);

	}

}
